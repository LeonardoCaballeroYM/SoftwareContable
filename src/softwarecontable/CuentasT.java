    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        double sumatoriaCargo = 0.0;
        int totalRowc = tbCuenta.getRowCount();
        totalRowc-=1;
        for (int i = 0; i <= (totalRowc); i++) {
            double sum = Double.parseDouble(String.valueOf(tbCuenta.getValueAt(i, 0)));
            sumatoriaCargo+=sum;
            txtSumCargo.setText(""+sumatoriaCargo);
        }
        
        double sumatoriaAbono = 0.0;
        int totalRowa = tbCuenta.getRowCount();
        totalRowa-=1;
        for (int i = 0; i <= (totalRowa); i++) {
            double sum = Double.parseDouble(String.valueOf(tbCuenta.getValueAt(i, 1)));
            sumatoriaAbono+=sum;
            txtSumAbono.setText(""+sumatoriaAbono);
        }
        
        if (sumatoriaCargo > sumatoriaAbono) {
            double sumaDeudor = sumatoriaCargo - sumatoriaAbono;
            txtSaldoDeudor.setText(""+sumaDeudor);
        } else if (sumatoriaCargo < sumatoriaAbono) {
            double sumaAcreedor = sumatoriaAbono - sumatoriaCargo;
            txtSaldoAcreedor.setText(""+sumaAcreedor);
        }
    }//GEN-LAST:event_btnResultadoActionPerformed

