    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String nombrecuenta = txtCuenta.getText();
        double sumcargar = Double.parseDouble(txtSumCargo.getText());
        double sumabono = Double.parseDouble(txtSumAbono.getText());
        int cuenta = Integer.parseInt(txtNoCuenta.getText());
        int subcuenta = Integer.parseInt(txtSubCuenta.getText());
        double parcial = Double.parseDouble(txtParcial.getText());
        String namecuenta = txtNomCuent.getText();
        String descripcion = txtDescripcion.getText();
        SaldoCuentaT saldoCuenta = new SaldoCuentaT(namecuenta, sumcargar, sumabono, cuenta,
                subcuenta, parcial, namecuenta, descripcion);
        SaldoCuenta.add(saldoCuenta);
        llenarTablaPolizaEgresos();
        JOptionPane.showMessageDialog(this, "Registro guardado correctamete");
    }//GEN-LAST:event_btnSaveActionPerformed

